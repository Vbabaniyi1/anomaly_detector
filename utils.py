# utils.py — Preprocessing and analysis utilities
# Designed for CWRU (training) and IMS (cross-machine evaluation)
import numpy as np
from scipy.io import loadmat
from scipy.signal import resample, welch
from scipy.stats import kurtosis, skew
import torch

WINDOW_SIZE  = 1024
CWRU_SR      = 12_000   # CWRU sample rate (Hz) — training domain
IMS_SR       = 20_480   # IMS sample rate (Hz)  — test domain

# ── IMS Dataset Constants ─────────────────────────────────────
# IMS Test 1: 4 bearings, 8 channels (2 per bearing)
# Bearing 3 (channels 5-6) develops outer race fault
# Files: space-separated ASCII, 20480 samples per channel per snapshot
IMS_BEARING_CHANNELS = {
    "Bearing 1": [0, 1],
    "Bearing 2": [2, 3],
    "Bearing 3": [4, 5],   # develops fault
    "Bearing 4": [6, 7],
}
IMS_FAULT_BEARING = "Bearing 3"


def zscore_normalise(window, eps=1e-8):
    """Per-window z-score normalisation."""
    mu, sigma = window.mean(), window.std()
    return (window - mu) / (sigma + eps)


def extract_windows(signal, window_size=WINDOW_SIZE, norm="perwindow"):
    """Extract non-overlapping windows from a signal."""
    windows = []
    for start in range(0, len(signal) - window_size + 1, window_size):
        w = signal[start: start + window_size].astype(np.float32)
        if norm == "perwindow":
            w = zscore_normalise(w)
        windows.append(w)
    return (np.array(windows, dtype=np.float32) if windows
            else np.empty((0, window_size), dtype=np.float32))


def resample_to_cwru(signal, src_sr, tgt_sr=CWRU_SR):
    """Resample signal to match CWRU sample rate (12 kHz)."""
    if src_sr == tgt_sr:
        return signal
    n_target = int(len(signal) * tgt_sr / src_sr)
    return resample(signal, n_target).astype(np.float64)


def batch_anomaly_scores(model, windows, device, batch_size=256):
    """Compute anomaly scores for all windows."""
    model.eval()
    t = torch.FloatTensor(windows).unsqueeze(1).to(device)
    scores = []
    with torch.no_grad():
        for i in range(0, len(t), batch_size):
            scores.append(
                model.anomaly_score(t[i: i + batch_size]).cpu().numpy())
    return np.concatenate(scores)


def get_reconstruction(model, window, device):
    """Get reconstruction of a single window."""
    model.eval()
    t = torch.FloatTensor(window).unsqueeze(0).unsqueeze(0).to(device)
    with torch.no_grad():
        rec = model(t).squeeze().cpu().numpy()
    return rec


